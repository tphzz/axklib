import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import openapiTS, { astToString } from 'openapi-typescript';
import { format, resolveConfig } from 'prettier';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = resolve(projectRoot, 'src/lib/generated/axklibApiV1.ts');
const candidates = [
  process.env.AXKLIB_OPENAPI_PATH,
  resolve(projectRoot, '../server/contracts/openapi-v1.json'),
].filter(Boolean);
const schemaPath = candidates.find((candidate) => existsSync(candidate));

if (!schemaPath) {
  throw new Error(
    `axklib OpenAPI contract not found; checked:\n${candidates.map((candidate) => `  ${candidate}`).join('\n')}`,
  );
}

const mode = process.argv[2];
if (mode !== 'check' && mode !== 'write') {
  throw new Error('usage: node tools/openapi-contract.mjs <check|write>');
}

const schema = JSON.parse(readFileSync(schemaPath, 'utf8'));
const domainOperationIds = new Set();
for (const pathItem of Object.values(schema.paths ?? {})) {
  for (const operation of Object.values(pathItem ?? {})) {
    for (const operationId of operation?.['x-axklib-operation-ids'] ?? []) {
      domainOperationIds.add(operationId);
    }
  }
}
if (domainOperationIds.size === 0) {
  throw new Error('axklib OpenAPI contract is not registry-expanded');
}
const nodes = await openapiTS(schema, { alphabetize: true });
const prettierConfig = (await resolveConfig(outputPath)) ?? {};
const generated = await format(
  [
    '// Generated from axklib/apps/server/contracts/openapi-v1.json by tools/openapi-contract.mjs.',
    '// Do not edit this file directly.',
    '',
    astToString(nodes).trimEnd(),
    '',
  ].join('\n'),
  { ...prettierConfig, filepath: outputPath },
);

if (mode === 'write') {
  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, generated);
} else {
  const existing = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';
  if (existing !== generated) {
    process.stderr.write(
      'generated axklib API types are stale; run `pnpm contract:generate` and commit the result\n',
    );
    process.exitCode = 1;
  }
}
